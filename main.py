n=int(input("Введите целое число")) # Ввод целого числа с клавиатуры пользоваелем
# Используем оператор if-else определяющи четность или не четность числа
if n % 2== 0: #условие для проверки числа на четность
    print("Число четное")# выведение на консоль 
else: # если число не соответстует условию, то выполняется следущее действие
    print("Число нечетное")# выведение на консоль